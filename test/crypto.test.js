describe('frozen test vectors', () => {
  for (const [name, v] of Object.entries(VECTORS)) {
    it(`${name}: canonical AAD matches`, () => {
      expect(hex(buildAAD(v.adata))).toBe(v.aadHex);
    });
    it(`${name}: CEK wrap matches and unwraps`, async () => {
      const aad = buildAAD(v.adata);
      const kek = await deriveKEK(F, { usePassword: v.usePassword, password: v.password, salt, iter: v.iter });
      const wk = await aesGcmEncrypt(kek, ivw, CEK, aad);
      expect(hex(wk)).toBe(v.wkHex);
      expect(hex(await aesGcmDecrypt(kek, ivw, wk, aad))).toBe(hex(CEK));
    });
    it(`${name}: content ciphertext matches and decrypts`, async () => {
      const aad = buildAAD(v.adata);
      const key = await crypto.subtle.importKey('raw', CEK, { name: 'AES-GCM' }, false, ['encrypt', 'decrypt']);
      const ct = await aesGcmEncrypt(key, ivc, utf8(VEC_PLAINTEXT), aad);
      expect(hex(ct)).toBe(v.ctHex);
      expect(fromUtf8(await aesGcmDecrypt(key, ivc, unhex(v.ctHex), aad))).toBe(VEC_PLAINTEXT);
    });
  }
});

describe('encrypt/decrypt round-trip', () => {
  it('no-password round-trips and emits a valid format-v1 body', async () => {
    const text = 'hello, binthere';
    const { body, fragment } = await encryptPaste({ text });
    expect(() => validatePaste(body)).not.toThrow();
    expect(body.adata.kdf).toBe('hkdf');
    const out = await decryptPaste({ paste: body, fragment });
    expect(out.text).toBe(text);
  });

  it('round-trips every format and the burn flag', async () => {
    for (const fmt of ['plaintext', 'code', 'markdown']) {
      for (const bar of [false, true]) {
        const text = `body ${fmt} ${bar}`;
        const { body, fragment } = await encryptPaste({ text, fmt, bar });
        const out = await decryptPaste({ paste: body, fragment });
        expect(out).toEqual({ text, fmt, bar });
      }
    }
  });

  it('the fragment secret never appears in the POST body', async () => {
    const { body, fragment } = await encryptPaste({ text: 'secret' });
    expect(JSON.stringify(body)).not.toContain(fragment);
  });
});

describe('password key hierarchy (SPEC.md §2)', () => {
  it('decrypts only with the correct fragment AND password', async () => {
    const text = 'top secret';
    const password = 'hunter2';
    const { body, fragment } = await encryptPaste({ text, password });
    expect(body.adata.kdf).toBe('pbkdf2-hkdf');
    const out = await decryptPaste({ paste: body, fragment, password });
    expect(out.text).toBe(text);
  });

  it('fragment alone is insufficient (no password → PasswordRequired)', async () => {
    const { body, fragment } = await encryptPaste({ text: 'x', password: 'pw' });
    await expect(decryptPaste({ paste: body, fragment })).rejects.toBeInstanceOf(PasswordRequired);
    await expect(decryptPaste({ paste: body, fragment, password: 'wrong' })).rejects.toBeInstanceOf(DecryptError);
  });

  it('password alone is insufficient (wrong fragment → DecryptError)', async () => {
    const { body, password } = { ...(await encryptPaste({ text: 'x', password: 'pw' })), password: 'pw' };
    const wrongFragment = b64urlFromBytes(fill(0x99, 32));
    await expect(decryptPaste({ paste: body, fragment: wrongFragment, password })).rejects.toBeInstanceOf(DecryptError);
  });
});

describe('deriveContentKey / decryptContent split (verify before consume)', () => {
  it('verifies the password independently of the content, then decrypts', async () => {
    const { body, fragment } = await encryptPaste({ text: 'split me', password: 'pw' });
    const cek = await deriveContentKey({ adata: body.adata, wk: body.wk, fragment, password: 'pw' });
    expect((await decryptContent({ adata: body.adata, ct: body.ct, cek })).text).toBe('split me');
  });

  it('throws on wrong password and PasswordRequired without one — without touching ct', async () => {
    const { body, fragment } = await encryptPaste({ text: 'x', password: 'pw' });
    await expect(deriveContentKey({ adata: body.adata, wk: body.wk, fragment, password: 'bad' }))
      .rejects.toBeInstanceOf(DecryptError);
    await expect(deriveContentKey({ adata: body.adata, wk: body.wk, fragment }))
      .rejects.toBeInstanceOf(PasswordRequired);
  });
});

describe('authentication / tamper (AAD + ciphertext binding)', () => {
  async function make() {
    return encryptPaste({ text: 'authentic', fmt: 'plaintext', bar: false });
  }
  it('flipping an authenticated adata field breaks decryption', async () => {
    for (const mutate of [
      (b) => { b.adata.bar = true; },
      (b) => { b.adata.fmt = 'markdown'; },
      (b) => { b.adata.comp = b.adata.comp === 'gzip' ? 'none' : 'gzip'; },
    ]) {
      const { body, fragment } = await make();
      mutate(body);
      await expect(decryptPaste({ paste: body, fragment })).rejects.toBeInstanceOf(DecryptError);
    }
  });
  it('flipping a ciphertext byte breaks decryption', async () => {
    const { body, fragment } = await make();
    const ct = bytesFromB64url(body.ct);
    ct[0] ^= 0x01;
    body.ct = b64urlFromBytes(ct);
    await expect(decryptPaste({ paste: body, fragment })).rejects.toBeInstanceOf(DecryptError);
  });
  it('swapping the IV breaks decryption', async () => {
    const { body, fragment } = await make();
    body.adata.ivc = b64urlFromBytes(fill(0x00, 12));
    await expect(decryptPaste({ paste: body, fragment })).rejects.toBeInstanceOf(DecryptError);
  });
});

describe('compression', () => {
  it('compresses compressible content and round-trips', async () => {
    const text = 'x'.repeat(50000);
    const { body, fragment } = await encryptPaste({ text });
    expect(body.adata.comp).toBe('gzip');
    expect((await decryptPaste({ paste: body, fragment })).text).toBe(text);
  });

  it('rejects oversized plaintext before encryption', async () => {
    const big = 'a'.repeat(MAX_PLAINTEXT + 1);
    await expect(encryptPaste({ text: big })).rejects.toThrow(/too large/);
  });
});

describe('decompression safety', () => {
  it('aborts a gzip bomb that exceeds the output cap', async () => {
    const bomb = await gzip(fill(0, 200000)); // 200 KB of zeros → tiny gzip
    await expect(gunzip(bomb, 1000)).rejects.toBeInstanceOf(DecryptError);
  });
  it('rejects malformed gzip input', async () => {
    await expect(gunzip(fill(0xab, 64))).rejects.toBeInstanceOf(DecryptError);
  });
});
